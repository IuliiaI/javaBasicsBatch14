		System.out.println("          Task 3   Way1    ");
		//Print even numbers from 20 to 1 (2 ways)
		
		for (int c=20; c>=1; c--) {
			if(c%2==0) {
				System.out.print(c+" ");
			}
		}
		System.out.println(" ");
		System.out.println("          Task 3    Way2   ");
		
