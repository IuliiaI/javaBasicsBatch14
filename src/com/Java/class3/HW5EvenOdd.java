package com.Java.class3;

public class HW5EvenOdd {

	public static void main(String[] args) {
		int number;
		number=9;
		
		int check=number%2;
		
		if(check==0) {
			System.out.println("The number " +number+" is even");
		}else {
			System.out.println("The number " +number+" is odd");
		}

	}

}
