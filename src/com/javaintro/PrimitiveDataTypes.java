package com.javaintro;

public class PrimitiveDataTypes {

	public static void main(String[] args) {
		//storing and representing whole number values;
		
		byte box1=20;
		short box2=100;
		int box3=100000; //popular
		long box4=10000000000L; 
		
		//storing and representing of decimal values;
		float variable1=1.99F;
		double variable2=99.99; //popular
		
		//storing single characters;
		char container='a';
		char dollar='$';
		
		//storing yes or no values
		boolean hot=true;
		boolean tired=false;
		
		
		System.out.println(box1);//printing value of box1
		System.out.println("box1");//printing text box1
		
		System.out.println(tired);
		//System.out.println(variable3);error-variable does not exist
		
		System.out.println(box1);
		System.out.println(box2);
		System.out.println(box3);
		System.out.println(box4);
		System.out.println(variable1);
		System.out.println(variable2);
		System.out.println(container);
		System.out.println(dollar);
		System.out.println(hot);
		System.out.println(tired);

	}

}
